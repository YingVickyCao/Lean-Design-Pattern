package com.hades.example.designpatterns.facade;

public class TV {
    public void on() {
        System.out.println("Dvd is On");
    }

    public void play() {
        System.out.println("Dvd is play");
    }

    public void pause() {
        System.out.println("Dvd is pause");
    }

    public void off() {
        System.out.println("Dvd is Off");
    }
}
